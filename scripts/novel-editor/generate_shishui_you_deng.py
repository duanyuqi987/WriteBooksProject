def expand_chapters() -> list[Chapter]:
    """Expand 40 core beats into 200 chapters with five narrative focuses."""
    chapters: list[Chapter] = []
    for beat in CORE_BEATS:
        for suffix, poem_kind in FOCUSES:
            index = len(chapters) + 1
            if suffix == "之前":
                title = f"{beat.title}之前"
                event = f"{beat.event}真正发生之前，陆允川已经在相似的日常里练习过很多次沉默。那些练习不够戏剧化，却把后来的选择悄悄垫高。"
                relation = f"这种变化先露出来：{beat.relation_shift.rstrip('。')}。只是他还不肯承认。"
            elif suffix == "当日":
                title = beat.title
                event = beat.event
                relation = beat.relation_shift
            elif suffix == "余波":
                title = f"{beat.title}之后"
                event = f"{beat.event}之后，陆允川把细节一遍遍放回心里比较，像检查一张做错的卷子，越检查越不肯交卷。"
                relation = beat.relation_shift.rstrip("。") + "，但余波比事件本身更慢，也更难收拾。"
            elif suffix == "旁观":
                title = f"{beat.title}旁观"
                event = f"从朋友和她的角度看，{beat.event}并不是单线条的伤心故事，而是几个人各自笨拙地保护自己。"
                relation = beat.relation_shift.rstrip("。") + "，旁观者看得更清楚，也更无能为力。"
            else:
                title = f"{beat.title}多年后"
                event = f"多年后陆允川重写这一段，才发现{beat.event}留下的不是判决，而是一盏很小的灯。"
                relation = beat.relation_shift.rstrip("。") + "，到了后来，它终于从伤口变成可以回看的旧路。"
            chapters.append(
                Chapter(
                    index=index,
                    part=beat.part,
                    title=title,
                    season=beat.season,
                    place=beat.place,
                    event=event,
                    object_detail=beat.object_detail,
                    relation_shift=relation,
                    friend_note=beat.friend_note,
                    female_note=beat.female_note,
                    ending_image=beat.ending_image,
                    focus=suffix,
                    poem_kind=poem_kind,
                )
            )
    return chapters


CHAPTERS = expand_chapters()


def pick(items: list[str], idx: int, offset: int = 0) -> str:
    """Pick a deterministic rotating item for chapter generation."""
    return items[(idx + offset) % len(items)]


def poem_for(chapter: Chapter) -> tuple[str, str]:
    """Create an original poem insert for one chapter."""
    idx = chapter.index
    if chapter.poem_kind == "现代诗":
        image = pick(MODERN_IMAGES, idx)
        text = "\n".join(
            [
                f"我把{image}旁的清晨擦亮，",
                "不问它从哪一年开始发凉。",
                "有人经过我的青春，",
                "像风经过一排没有关紧的窗。",
                "我终于学会不把名字攥疼，",
                "只让灯亮一会儿，再让天亮。"
            ]
        )
        return "原创现代诗", text
    if chapter.poem_kind == "仿唐绝句":
        image = pick(TANG_IMAGES, idx)
        text = "\n".join(
            [
                f"{image}微明照旧尘，",
                "少年心事隔江春。",
                "回看不见当时路，",
                "一树风声认故人。"
            ]
        )
        return "原创仿唐绝句", text
    image = pick(SONG_IMAGES, idx)
    text = "\n".join(
        [
            f"{image}轻，旧梦醒，",
            "小窗深处有余灯。",
            "问少年、几番执念，几番无凭。",
            "人去不须追远影，",
            "水流仍向明朝生。"
        ]
    )
    return "原创仿宋小令", text


