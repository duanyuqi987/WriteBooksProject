def replace_chapter_body(html, ch_id, new_body_html):
    """Replace everything between </h2> and next chapter/footer"""
    pos = html.find(f'<h2 id="{ch_id}">')
    if pos < 0:
        print(f'  ERROR: Cannot find <h2 id="{ch_id}">')
        return html, False

    h2_end = html.find('</h2>', pos) + len('</h2>')

    next_pos = html.find('<h2 id="ch', h2_end)
    if next_pos < 0:
        next_pos = html.find('<footer', h2_end)
    if next_pos < 0:
        print(f'  ERROR: Cannot find end of {ch_id}')
        return html, False

    html = html[:h2_end] + new_body_html + html[next_pos:]
    return html, True

def end_section():
    return '\n\n    <p style="text-align:center;text-indent:0;color:var(--muted);">（完）</p>\n\n'

