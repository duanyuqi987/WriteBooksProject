def insert_paragraph(html, ch_id, para_text):
    """在章节end marker前插入段落"""
    pos = html.find(f'<h2 id="ch{ch_id}">')
    if pos < 0:
        return html, False
    # Find end marker
    content_end = html.find('（完）', pos)
    if content_end < 0:
        # Try finding next chapter
        next_h2 = html.find('<h2 id="ch', pos + 20)
        if next_h2 < 0:
            next_h2 = html.find('<footer', pos)
            if next_h2 < 0:
                next_h2 = html.find('<div class="divider">', pos)
        content = html[pos:next_h2] if next_h2 > 0 else html[pos:]
        last_p = content.rfind('</p>')
        if last_p > 0:
            content_end = pos + last_p
    if content_end < 0:
        return html, False
    p = f'\n    <p>{para_text}</p>\n'
    return html[:content_end] + p + html[content_end:], True

def insert_after_h2(html, ch_id, para_text):
    """在H2标题后插入段落（作为章节第一段）"""
    pos = html.find(f'<h2 id="ch{ch_id}">')
    if pos < 0:
        return html, False
    h2_end = html.find('</h2>', pos)
    if h2_end < 0:
        return html, False
    insert_at = h2_end + len('</h2>')
    p = f'\n\n    <p>{para_text}</p>\n'
    return html[:insert_at] + p + html[insert_at:], True

def replace_text(html, old, new):
    """安全替换文本，检查是否存在"""
    if old in html:
        return html.replace(old, new, 1), True
    return html, False

