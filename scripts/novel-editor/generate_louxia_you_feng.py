def foreshadow(name: str, body: str) -> str:
    return dedent(
        f"""
        # {name}

        - status: paid_off
        - level: thematic
        - body: {body}
        """
    )


def combined_book(chapter_texts: list[str]) -> str:
    toc = ["# 楼下有风", "", "一部关于校园、日记本、未接电话、多年后释怀和青春回声的温柔现实小说。", "", "## 目录"]
    for c in CHAPTERS:
        toc.append(f"- 第{c.index:02d}章 {c.title}")
    return "\n".join(toc) + "\n\n---\n\n" + "\n\n---\n\n".join(chapter_texts)


