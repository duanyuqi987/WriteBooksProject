def expand_chapters(base_chapters: list[Chapter]) -> list[Chapter]:
    """Expand 40 core beats into a 120-chapter, 300k-word structure."""
    expanded: list[Chapter] = []
    suffixes = ("之前", "", "之后")
    for base in base_chapters:
        clean_relation = base.relation_shift.rstrip("。")
        for beat in range(3):
            index = len(expanded) + 1
            suffix = suffixes[beat]
            title = base.title if not suffix else f"{base.title}{suffix}"
            if beat == 0:
                season = f"{base.season}前后"
            elif beat == 1:
                season = base.season
            else:
                season = f"{base.season}过后"
            place = base.place
            if beat == 0:
                event = f"{base.event}这件事真正发生前，许亦安先在与之相近的日常里经历了一段不起眼的等待；那段等待不够戏剧化，却把后来的一切悄悄垫高。"
                relation_shift = f"{clean_relation}的影子先露出来，只是他还把它当成普通心动。"
                friend_note = f"{base.friend_note}他说得很轻，像怕一句重话把朋友推得更远。"
                ending = f"{base.ending_image}那画面还没有成为记忆，只是很安静地停在那里。"
            elif beat == 1:
                event = base.event
                relation_shift = base.relation_shift
                friend_note = base.friend_note
                ending = base.ending_image
            else:
                event = f"{base.event}之后，许亦安反复回想当天的细枝末节，把能记住的都写进日记本，也把不能说出口的留在空行里。"
                relation_shift = f"{clean_relation}，但他要到更晚才肯承认这件事已经改变了他们之间的位置。"
                friend_note = f"{base.friend_note}这句话在当时没有用，后来却常常从记忆里浮出来。"
                ending = f"{base.ending_image}那画面后来被他想起很多次，每一次都比第一次轻一点。"
            expanded.append(
                Chapter(
                    index,
                    base.part,
                    title,
                    season,
                    place,
                    event,
                    base.object_detail,
                    relation_shift,
                    friend_note,
                    ending,
                )
            )
    return expanded


