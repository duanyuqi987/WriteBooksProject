def replace_in_chapter(html, ch_id, replacements):
    pos = html.find(f'<h2 id="ch{ch_id}">')
    if pos < 0: return html, 0
    next_pos = html.find('<h2 id="ch', pos + 20)
    if next_pos < 0: next_pos = html.find('<footer', pos)
    if next_pos < 0: next_pos = len(html)
    ch_content = html[pos:next_pos]
    count = 0
    for old, new in replacements:
        if old in ch_content:
            ch_content = ch_content.replace(old, new)
            count += 1
        else:
            print(f'  MISS ch{ch_id}: {old[:50]}...')
    return html[:pos] + ch_content + html[next_pos:], count

