def build_book() -> None:
    """Generate project docs, chapter files, and the combined book."""
    for directory in (ROOT, TEXT_DIR, CHAR_DIR, FORESHADOW_DIR):
        directory.mkdir(parents=True, exist_ok=True)

    write(ROOT / "project.md", project_doc())
    write(ROOT / "outline.md", outline_doc())
    write(ROOT / "project-state.md", state_doc())
    write(ROOT / "memory-snapshot.md", memory_doc())
    write(ROOT / "privacy-review.md", privacy_doc())
    write(FORESHADOW_DIR / "foreshadow-001.md", foreshadow_doc())
    write(ROOT / "poetry-index.md", poetry_index_doc())
    for name, content in character_docs().items():
        write(CHAR_DIR / name, content)

    combined = [f"# {TITLE}", "", f"作者：{AUTHOR}", "", f"> 公开版来源说明：{SOURCE_LABEL}", ""]
    for chapter in CHAPTERS:
        body = chapter_body(chapter)
        write(TEXT_DIR / f"chapter-{chapter.index:03d}.md", body)
        combined.append(body)
        combined.append("")
    write(TEXT_DIR / f"{TITLE}.md", "\n".join(combined))


