def insert_paragraph(html, ch_id, para_text):
    """在章节end marker前插入段落"""
    pos = html.find(f'<h2 id="ch{ch_id}">')
    if pos < 0:
        return html, False
    content_end = html.find('（完）', pos)
    if content_end < 0:
        next_h2 = html.find('<h2 id="ch', pos + 20)
        if next_h2 < 0:
            next_h2 = html.find('<footer', pos)
            if next_h2 < 0:
                next_h2 = html.find('<div class="divider">', pos)
        content = html[pos:next_h2] if next_h2 > 0 else html[pos:]
        last_p = content.rfind('</p>')
        if last_p > 0:
            content_end = pos + last_p
    if content_end < 0:
        return html, False
    p = f'\n    <p>{para_text}</p>\n'
    return html[:content_end] + p + html[content_end:], True

def insert_inner_monologue(html, ch_id, inner_text):
    """在end marker前插入inner-monologue"""
    pos = html.find(f'<h2 id="ch{ch_id}">')
    if pos < 0:
        return html, False
    content_end = html.find('（完）', pos)
    if content_end < 0:
        return html, False
    div = f'\n    <div class="inner-monologue">\n      <p>{inner_text}</p>\n    </div>\n'
    return html[:content_end] + div + html[content_end:], True

